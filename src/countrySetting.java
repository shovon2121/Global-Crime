    private void btnseaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnseaActionPerformed
        // TODO add your handling code here:
         
        
         String ShowQuery ="Select * From countrytbl Where Country='"+txtsea.getText().toString()+"'"; 
        con = new Dbconnect();
        try {
            ResultSet rs = con.ShowData(ShowQuery);
            
            
            while(rs.next()){
               txtcou.setText(rs.getString(1));
               txtcur.setText(rs.getString(2));
               txtare.setText(rs.getString(3));
               txtpop.setText(rs.getString(4));
               txtper.setText(rs.getString(5));
               
                
            }
            
            
            
            
        } catch (Exception ex) {
           
        }
        
        
        
        
        
        
    }//GEN-LAST:event_btnseaActionPerformed
