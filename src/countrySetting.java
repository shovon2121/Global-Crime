    private void btnupdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdActionPerformed
        // TODO add your handling code here:
        
        
        
        String UpdateQuery = "Update countrytbl SET Country='"+txtcou.getText().toString()+"',Currency='"+txtcur.getText().toString()+"',Area='"+txtare.getText().toString()+"',Population='"+txtpop.getText().toString()+"',PerCapitaGDP='"+txtper.getText().toString()+"' Where Country='"+txtsea.getText().toString()+"'";
        
         con = new Dbconnect();
        
        try {
            con.ExeQuery(UpdateQuery);
        } catch (Exception ex) {
            Logger.getLogger(countrySetting.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(this,"DATA UPDATED");
        txtcou.setText("");
        txtcur.setText("");
        txtare.setText("");
        txtpop.setText("");
        txtper.setText("");
        txtsea.setText("");
        
        
        
        
        
    }//GEN-LAST:event_btnupdActionPerformed
